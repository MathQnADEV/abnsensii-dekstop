    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // 1. Ambil dan validasi data dari form
        String namaDosen = txtNamaDosen.getText().trim();
        String nidn = txtNidn.getText().trim();

        if (namaDosen.isEmpty() || nidn.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Dosen dan NIDN harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 2. Buat objek model
        DosenModel newDosen = new DosenModel(nidn, namaDosen);

        // 3. Panggil DAO untuk menambah data
        boolean isSuccess = dosenDAO.addDosen(newDosen);

        // 4. Tampilkan pesan dan refresh
        if (isSuccess) {
            JOptionPane.showMessageDialog(this, "Data dosen berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            clearForm();
            loadDataToTable();
            EventBus.notifyDataChanged();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menambah data. Kemungkinan NIDN sudah terdaftar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // 1. Validasi
        if (!isEditing) {
            JOptionPane.showMessageDialog(this, "Pilih data di tabel terlebih dahulu sebelum mengubah!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 2. Ambil dan validasi data dari form
        String namaDosen = txtNamaDosen.getText().trim();
        String nidn = txtNidn.getText().trim();

        if (namaDosen.isEmpty() || nidn.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Dosen dan NIDN harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 3. Buat objek model dan set ID-nya
        DosenModel dosenToUpdate = new DosenModel(nidn, namaDosen);
        dosenToUpdate.setIdDosen(editingIdDosen);

        // 4. Panggil DAO untuk mengupdate data
        boolean isSuccess = dosenDAO.updateDosen(dosenToUpdate);

        // 5. Tampilkan pesan berdasarkan hasil
        if (isSuccess) {
            JOptionPane.showMessageDialog(this, "Data dosen berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            resetFormState();
            loadDataToTable();
            EventBus.notifyDataChanged();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data. Kemungkinan NIDN sudah digunakan.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // 1. Ambil baris yang dipilih di tabel
        int selectedRow = tblDosen.getSelectedRow();

        // 2. Validasi
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data dosen yang ingin dihapus terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3. Ambil data dosen dari list berdasarkan baris yang dipilih
        DosenModel selectedDosen = listDosenData.get(selectedRow);
        int idDosen = selectedDosen.getIdDosen();
        String namaDosen = selectedDosen.getNamaDosen();

        // 4. Tampilkan dialog konfirmasi
        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Apakah Anda yakin ingin menghapus dosen '" + namaDosen + "'?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
        );

        // 5. Jika user menekan tombol "YES"
        if (confirm == JOptionPane.YES_OPTION) {
            // Hapus data melalui DAO
            dosenDAO.deleteDosen(idDosen);

            // Tampilkan pesan sukses
            JOptionPane.showMessageDialog(this, "Data dosen berhasil dihapus.", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            // 6. Refresh tampilan
            clearForm();
            loadDataToTable();
            EventBus.notifyDataChanged();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

