    private void loadDataToTable() {
        listDosenData = dosenDAO.getAllDosen();
        DefaultTableModel model = (DefaultTableModel) tblDosen.getModel();
        model.setRowCount(0);

        int no = 1;
        for (DosenModel dosen : listDosenData) {
            Object[] rowData = {
                no++ + ".",
                dosen.getNamaDosen(),
                dosen.getNidn()
            };
            model.addRow(rowData);
        }
    }

    private void clearForm() {
        txtNamaDosen.setText("");
        txtNidn.setText("");
    }

    private void loadFormDataToForm() {
        int selectedRow = tblDosen.getSelectedRow();
        if (selectedRow != -1) {
            // Ambil data dari list berdasarkan baris yang dipilih
            DosenModel selectedDosen = listDosenData.get(selectedRow);

            // Isi form dengan data
            txtNamaDosen.setText(selectedDosen.getNamaDosen());
            txtNidn.setText(selectedDosen.getNidn());

            // Aktifkan mode edit
            isEditing = true;
            editingIdDosen = selectedDosen.getIdDosen();
        }
    }

    private void resetFormState() {
        clearForm();
        isEditing = false; 
        editingIdDosen = -1; 
    }

    private void filterTable() {
        // 1. Ambil teks dari field pencarian
        String searchText = txtCariNamaDosen.getText().trim().toLowerCase();

        // 2. Buat model baru for tabel yang sudah difilter
        DefaultTableModel filteredModel = new DefaultTableModel(
                new Object[][]{},
                new String[]{"No.", "Nama Dosen", "NIDN"}
        );

        // 3. Loop melalui data master
        int no = 1;
        for (DosenModel dosen : listDosenData) {
            // 4. Cek apakah nama dosen mengandung teks pencarian
            if (dosen.getNamaDosen().toLowerCase().contains(searchText)) {
                Object[] rowData = {
                    no++ + ".",
                    dosen.getNamaDosen(),
                    dosen.getNidn()
                };
                filteredModel.addRow(rowData);
            }
        }

        // 5. Set model baru ke tabel
        tblDosen.setModel(filteredModel);
    }

    @Override
    public void onDataChanged() {
        loadDataToTable();
    }

