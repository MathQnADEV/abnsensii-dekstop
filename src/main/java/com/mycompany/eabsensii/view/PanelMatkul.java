    private void loadDataToTable() {
        listMataKuliahData = matkulDAO.getAllMataKuliah();
        DefaultTableModel model = (DefaultTableModel) tblMataKuliah.getModel();
        model.setRowCount(0);

        int no = 1;
        for (MataKuliahModel mk : listMataKuliahData) {
            Object[] rowData = {
                no++ + ".",
                mk.getNamaMk(),
                mk.getKodeMk(),
                mk.getSks()
            };
            model.addRow(rowData);
        }
    }

    private void clearForm() {
        txtNamaMataKuliah.setText("");
        txtKodeMK.setText("");
        txtJumlahSKS.setText("");
    }

    @Override
    public void setVisible(boolean aFlag) {
        super.setVisible(aFlag);
        if (aFlag && isFirstTime) {
            loadDataToTable(); 
            isFirstTime = false; 
        }
    }

    private void loadFormDataToForm() {
        int selectedRow = tblMataKuliah.getSelectedRow();
        if (selectedRow != -1) {
            MataKuliahModel selectedMk = listMataKuliahData.get(selectedRow);

            txtNamaMataKuliah.setText(selectedMk.getNamaMk());
            txtKodeMK.setText(selectedMk.getKodeMk());
            txtJumlahSKS.setText(String.valueOf(selectedMk.getSks()));

            isEditing = true;
            editingIdMk = selectedMk.getIdMk();
        }
    }

    private void resetFormState() {
        clearForm();
        isEditing = false;
        editingIdMk = -1;
    }

    private void filterTable() {
        // 1. Ambil teks dari field pencarian
        String searchText = txtCariNamaMataKuliah.getText().trim().toLowerCase();

        // 2. Buat model baru
        DefaultTableModel filteredModel = new DefaultTableModel(
                new Object[][]{},
                new String[]{"No", "Nama Mata Kuliah", "Kode MK", "Jumlah SKS"}
        );

        // 3. Loop melalui data master
        int no = 1;
        for (MataKuliahModel mk : listMataKuliahData) {
            // 4. Cek apakah nama mata kuliah mengandung (case-insensitive)
            if (mk.getNamaMk().toLowerCase().contains(searchText)) {
                Object[] rowData = {
                    no++ + ".",
                    mk.getNamaMk(),
                    mk.getKodeMk(),
                    mk.getSks()
                };
                filteredModel.addRow(rowData);
            }
        }

        // 5. Set model baru ke tabel
        tblMataKuliah.setModel(filteredModel);
    }

    @Override
    public void onDataChanged() {
        loadDataToTable();
    }

