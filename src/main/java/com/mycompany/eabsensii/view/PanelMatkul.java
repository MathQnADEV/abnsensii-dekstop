    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // 1. validasi
        String namaMk = txtNamaMataKuliah.getText().trim();
        String kodeMk = txtKodeMK.getText().trim();
        String sksText = txtJumlahSKS.getText().trim();

        if (namaMk.isEmpty() || kodeMk.isEmpty() || sksText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int sks;
        try {
            sks = Integer.parseInt(sksText);
            if (sks <= 0) {
                JOptionPane.showMessageDialog(this, "Jumlah SKS harus angka positif!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah SKS harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 2. Buat objek model
        MataKuliahModel newMk = new MataKuliahModel(kodeMk, namaMk, sks);

        // 3. Panggil DAO untuk menambah data
        boolean isSuccess = matkulDAO.addMataKuliah(newMk);

        // 4. Tampilkan pesan dan refresh
        if (isSuccess) {
            JOptionPane.showMessageDialog(this, "Data mata kuliah berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            clearForm();
            loadDataToTable();
            EventBus.notifyDataChanged();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menambah data. Kemungkinan kode mata kuliah sudah ada.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // 1. Validasi
        if (!isEditing) {
            JOptionPane.showMessageDialog(this, "Pilih data di tabel terlebih dahulu sebelum mengubah!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 2. Ambil dan validasi
        String namaMk = txtNamaMataKuliah.getText().trim();
        String kodeMk = txtKodeMK.getText().trim();
        String sksText = txtJumlahSKS.getText().trim();

        if (namaMk.isEmpty() || kodeMk.isEmpty() || sksText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int sks;
        try {
            sks = Integer.parseInt(sksText);
            if (sks <= 0) {
                JOptionPane.showMessageDialog(this, "Jumlah SKS harus angka positif!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah SKS harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 3. Buat objek model dan set ID-nya
        MataKuliahModel mkToUpdate = new MataKuliahModel(kodeMk, namaMk, sks);
        mkToUpdate.setIdMk(editingIdMk);

        // 4. Panggil DAO
        boolean isSuccess = matkulDAO.updateMataKuliah(mkToUpdate);

        // 5. Tampilkan pesan berdasarkan hasil
        if (isSuccess) {
            JOptionPane.showMessageDialog(this, "Data mata kuliah berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            resetFormState();
            loadDataToTable();
            EventBus.notifyDataChanged();
        } else {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data. Kemungkinan kode atau nama mata kuliah sudah digunakan.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // 1. Ambil baris yang di Highlight tabel
        int selectedRow = tblMataKuliah.getSelectedRow();

        // 2. Validasi
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data mata kuliah yang ingin dihapus terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3. Ambil data mata kuliah dari list yang dipilih
        MataKuliahModel selectedMk = listMataKuliahData.get(selectedRow);
        int idMk = selectedMk.getIdMk();
        String namaMk = selectedMk.getNamaMk();

        // 4. dialog konfirmasi
        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Apakah Anda yakin ingin menghapus mata kuliah '" + namaMk + "'?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
        );

        // 5. Confirmation
        if (confirm == JOptionPane.YES_OPTION) {
            matkulDAO.deleteMataKuliah(idMk);

            JOptionPane.showMessageDialog(this, "Data mata kuliah berhasil dihapus.", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            // 6. Refresh tampilan
            clearForm();
            loadDataToTable();
            EventBus.notifyDataChanged();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

